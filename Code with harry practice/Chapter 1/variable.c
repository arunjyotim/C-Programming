#include <stdio.h>

int main()
{

    int a = 4;
    float b = 5.5;
    char c = 'u';
    int d = 34;
    int e = 45 + 9;

    printf("The value of a is %d\n", a);
    printf("The value of a is %.2f\n", b);
    printf("The value of a is %c\n", c);
    printf("Sum of a and d is %f\n", a + d);
    printf("Sum of a and e is %d", e);
    return 0;
}